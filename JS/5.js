let integer = 10; // 10진수
let hex = 0xa; // 16진수
let binary = 0b1010; // 2진수
let octal = 0o12; // 8진수
document.write(integer,'\n', hex,'\n', binary,'\n', octal,'\n');

let negative = -10; // 음수
let indices = 1.0e1; // 지수
let double = 10.12; // 소수
document.write(negative,'\n', indices,'\n', double,'\n');

let isInfinity = 10/0;
let isNan = 10/'몇';
document.write(isInfinity, '\n',isNan,'\n');

